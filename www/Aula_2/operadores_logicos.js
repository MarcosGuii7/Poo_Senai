// Igualdade de valor (==)
console.log(5==5);

//diferença de valor (!=)
console.log(5!=5);

//maior que (>)
console.log(10 > 7);

//Menor que (<)
console.log(3 < 5);

//Maior ou igual a (>=)
console.log(7 >= 7);

//Menor ou igual a (<=)
console.log(4 <= 9);

// E (&&)
console.log(5 == 5 && 10 > 7);

//Ou (||)
console.log(5 == 5 || 5 > 7);

